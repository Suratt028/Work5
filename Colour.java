


public class Colour {

    private int red;
    private int green;
    private int blue;

    public Colour() {
        this.red = 0;
        this.green = 0;
        this.blue = 0;
    }
    public int getRed() {
        return red;
    }

    public void setRed(int red) {
        this.red = red;
    }

    public int getGreen() {
        return green;
    }

    public void setGreen(int green) {
        this.green = green;
    }

    public int getBlue() {
        return blue;
    }

    public void setBlue(int blue) {
        this.blue = blue;
    }
   
}

